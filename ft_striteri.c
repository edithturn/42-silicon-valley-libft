void ft_striteri(char *s, void (*f)(unsigned int, char *))
{
	int i;
	unsigned index;

	index = 0;
	if(s != NULL && f != NULL)
	{
		i = 0;
		while(s[i])
		{
			f(index, s[i]);
			i++;
		}
	}
}
